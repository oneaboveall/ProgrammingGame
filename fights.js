function fightRd() {
  orc.hp=5
  if (player.atk+d20() >= orcAtk()){
    player.atk++;
    sFight++;
    ID();
    document.getElementById('storyDiv').innerHTML = '<p>You have defeated the orc!</p><input type="button" value="Keep going" onclick="headout()"></input>'
    } else {
    dead();
    }
}

